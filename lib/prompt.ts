export function buildUserPrompt(args: {
  caseTypeId: CaseTypeId;
  slots: Record<string, string>;
  isSpecialEd: boolean;
  liveLaw: string | null;
}): string {
  const type = getCaseType(args.caseTypeId);
  const lines: string[] = [];
  lines.push(`사안 유형: ${type.name} (권장 분량 ${type.lengthHint})`);
  lines.push(`참고 골격(그대로 베끼지 말고 입력값으로 자연스럽게 재작성): ${type.skeleton}`);
  lines.push('');
  lines.push('정적 근거 조문(참고):');
  for (const b of type.bases) lines.push(`- ${b}`);
  lines.push('');
  if (args.liveLaw) {
    lines.push('실시간 검증된 현행 법령(법제처 대조 결과, [근거]에 정확한 법령명·시행일을 반영):');
    lines.push(args.liveLaw);
    lines.push('');
  }
  if (args.isSpecialEd) {
    lines.push('이 사안의 대상 학생은 특수교육대상자이며 문제행동이 반복·심각하다. [근거]에 "교원의 학생생활지도에 관한 고시 제15조③(특수교육대상자의 심각한 문제행동은 개별화교육계획에 행동중재지원 사항 포함)"을 함께 인용하고, [향후 안전한 지도 방법]에 특수교육 지원팀·행동중재전문가 연계와 개별화교육계획(IEP) 갱신 요청을 포함한다.');
    lines.push('');
  }
  lines.push('입력된 사실(슬롯):');
  for (const slot of type.slots) {
    const v = args.slots[slot.key];
    if (v && v.trim() !== '') lines.push(`- ${slot.label}: ${v.trim()}`);
  }
  lines.push('');
  lines.push('위 사실만으로 누가기록을 작성한다. 입력에 없는 사실을 지어내지 않는다. JSON 객체 하나만 출력한다.');
  return lines.join('\n');
}
